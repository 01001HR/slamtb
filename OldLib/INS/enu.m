% ENU  {East North Up} local reference frame
%   NOTE: ENU is not a function or script. This is just a help file
%   exlaining what ENU means.
%
%   ENU is a local reference coordinate frame satisfying the right hand
%   rule which is intended for terrestrial navigation. It is fixed at some
%   point in the earth surface, with its three coordinate axes defined as
%       X: East
%       Y: North
%       Z: Up
%
%   Using ENU permits a very straightforward passage from and to UTM
%   coordinates. X is East UTM [m], Y is North UTM [m], and Z is the
%   altitude above sea level [m].
